function getChainDetails(chain: any) {
  const { id, name, nativeCurrency, blockExplorers, rpcUrls } = chain;
  const chainInfo = {
    chainId: id,
    name,
    currency: nativeCurrency.symbol,
    explorerUrl: blockExplorers === undefined ? "" : blockExplorers.default.url,
    rpcUrl: rpcUrls.default.http[0],
  };

  return chainInfo;
}

